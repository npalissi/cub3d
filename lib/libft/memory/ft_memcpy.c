#include "../libft.h"

void	*ft_memcpy(void *dest, const void *src, size_t n)
{
	void	*clone;

	if (!dest && !src)
		return (dest);
	clone = dest;
	while (n--)
		*(unsigned char *)dest++ = *(unsigned char *)src++;
	return (clone);
}
